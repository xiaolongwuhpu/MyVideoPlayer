public class Result {
    private String id;

    private String name;

    private String fid;

    public void setId(String id){
        this.id = id;
    }
    public String getId(){
        return this.id;
    }
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }
    public void setFid(String fid){
        this.fid = fid;
    }
    public String getFid(){
        return this.fid;
    }
}
